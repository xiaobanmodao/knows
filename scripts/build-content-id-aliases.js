const fs = require('fs');
const path = require('path');

const { chapterCatalog } = require('../packages/math/data/math-curriculum');

function getStableLessonId(chapterId, sectionTitle) {
  const normalizedTitle = String(sectionTitle || '')
    .replace(/^\d+(?:\.\d+)*\s*/, '')
    .replace(/[\s—–（）()·，、：:]/g, '');
  let hash = 2166136261;

  for (let index = 0; index < normalizedTitle.length; index += 1) {
    hash ^= normalizedTitle.charCodeAt(index);
    hash = Math.imul(hash, 16777619);
  }

  return `${chapterId}-lesson-s${(hash >>> 0).toString(36)}`;
}

const aliases = {};

chapterCatalog.forEach((chapter) => {
  chapter.officialSections.forEach((sectionTitle, index) => {
    aliases[`${chapter.id}-lesson-${index + 1}`] = getStableLessonId(chapter.id, sectionTitle);
  });
});

const output = [
  '// Generated by scripts/build-content-id-aliases.js. Do not edit manually.',
  `const LEGACY_KNOWLEDGE_ALIASES = ${JSON.stringify(aliases, null, 2)};`,
  '',
  'module.exports = { LEGACY_KNOWLEDGE_ALIASES };',
  '',
].join('\n');

fs.writeFileSync(path.join(__dirname, '../data/content-id-aliases.js'), output);
console.log(`Generated ${Object.keys(aliases).length} legacy knowledge aliases.`);
